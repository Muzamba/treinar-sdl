Map::Map(){
    terra = TextureManager::LoadTexture("../assets/terra.png");
    grama = TextureManager::LoadTexture("../assets/grama.png");
    agua = TextureManager::LoadTexture("../assets/agua.png");

    LoadMap(lv1);
    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;

    dest.x = dest.y = 0;

}

void Map::LoadMap(int arr[20][25]){
    for(int row = 0;row < 20;++row){
        for(int column = 0; column < 25; ++column){
            map[row][column] = arr[row][column];
        }
    }

}

void Map::DrawMap(){
    int type = 0;
    for(int row = 0;row < 20;++row){
        for(int column = 0; column < 25; ++column){
            type = map[row][column];
            dest.x = column * 32;
            dest.y = row * 32;


            switch(type){
                case 0:
                    TextureManager::Draw(agua, src, dest);
                    break;
                case 1:
                    TextureManager::Draw(grama, src, dest);
                    break;
                case 2:
                    TextureManager::Draw(terra, src, dest);
                    break;
            }
        }
    }


}
